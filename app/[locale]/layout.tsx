import type { Metadata } from 'next'
import { Rakkas as FontHeadingAr, Lateef as FontBaseAr, DM_Serif_Display as FontHeadingEn, DM_Sans as FontBaseEn } from 'next/font/google'
import '../globals.css'
import { NextIntlClientProvider } from 'next-intl';
import { notFound } from 'next/navigation';
import { ThemeProvider } from "@/app/components/theme-provider"
import { cn } from "@/lib/utils"
import ThemeCustomSwitch from '../components/theme-custom-switch';
import LocaleSwitch from '../components/locale-switch';
import MahmoudAbdelazizLogo from '../components/mahmoud-abdelaziz-logo';
import Navbar from '../components/nav-bar';

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
}
const fontBaseEn = FontBaseEn({ subsets: ['latin'], variable: '--font-base-en', weight: '400' })
const fontHeadingEn = FontHeadingEn({ subsets: ['latin'], variable: '--font-heading-en', weight: '400' })
const fontBaseAr = FontBaseAr({ subsets: ['arabic'], variable: '--font-base-ar', weight: '400' })
const fontHeadingAr = FontHeadingAr({ subsets: ['arabic'], variable: '--font-heading-ar', weight: '400' })

export default async function RootLayout({
  children,
  params: { locale }
}: {
  children: React.ReactNode,
  params: { locale: string }
}) {
  let messages;
  try {
    messages = (await import(`../../messages/${locale}.json`)).default;
  } catch (error) {

    notFound();
  }

  return (
    <html lang={locale} dir={locale === 'ar-EG' ? 'rtl' : 'ltr'} suppressHydrationWarning>
      <body
        className={cn(
          "min-h-screen bg-background rtl:font-baseAr ltr:font-baseEn  antialiased",
          `${fontBaseAr.variable} ${fontHeadingAr.variable} ${fontBaseEn.variable} ${fontHeadingEn.variable}  `
        )}
      >
        <NextIntlClientProvider locale={locale} messages={messages}>
          <ThemeProvider
            attribute="class"
            defaultTheme="system"
            enableSystem
            disableTransitionOnChange
          >
            <Navbar locale={locale} />
            <main className="container max-w-3xl p-4 mx-auto">
              {children}
            </main>
          </ThemeProvider>
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
